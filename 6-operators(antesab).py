#Assignment operators
#عملگر های انتسابی
#   =    +=    -=    *=    /=    =%     //=     **=     &=     |=    ^=     >>=      <<=

# =
p = 6
print("p= ",p) # ---> 6

print("-"*40)

# +=
q = 7
q += 1 # -----> q = q + 1
print("q= ",q)

print("-"*40)

# -=
w = 7
w -=1 # -----> w = w - 1
print("w= ",w)

print("-"*40)

# *=
e = 7
e *= 2 # -----> e = e * 1
print("e= ",e)

print("-"*40)

# /=
r = 8
r /= 2 # -----> r = r / 1
print("r= ",r)

print("-"*40)

# %=
t = 10
t %= 6 # -----> t = t % 1
print("t= ",(t))

print("-"*40)

# //=
y=15
y //= 8 # -----> y = y // 1
print("y= ",y)

print("-"*40)

# **=
u = 3
u **= 4 # -----> u = u ** 1
print("u= ",u)

print("-"*40)

# &= 
i = 3
i &= 4 # -----> i = i & 1
print(i) # ---> operators bit 

print("-"*40)
 
# |=
o = 3
o |= 4 # -----> o = o | 1
print(o)

print("-"*40)

# ^=
p = 3
p ^= 9 # -----> p = p ^ 1
print(p)

print("-"*40)

# >>=
a = 12
a >>= 1 # -----> a = a >> 1
print(a)

print("-"*40)
 
# <<= 
s = 12 # -----> s = s << 1
s <<= 1
print(s)

print("-"*40)

